  ab initio </i>atomistic simulations as implemented in a number of widely
  available software codes. Most often these tools are used as 'black boxes'
  with little or only partial knowledge of the practical implementation of the
  general theoretical ideas they are based on. This is a particularly severe
  drawback when new theoretical developments, analysis tools or computational
  experiments can not be pursued due to the lack of insight on the internal
  structure of the employed research software. This course, as a special course
  in the computational physics, is to teach students how the density functional
  theory become a code, which can be used as a powerful tool to study the
  electronic structure of solid.</span></p>
